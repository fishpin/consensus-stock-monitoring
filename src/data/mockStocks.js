/** Generate synthetic intraday price history for charting */
function generateHistory(basePrice, days = 30) {
  const history = [];
  let price = basePrice;
  const now = new Date();

  for (let i = days; i >= 0; i--) {
    const date = new Date(now);
    date.setDate(now.getDate() - i);
    const change = (Math.random() - 0.48) * (basePrice * 0.025);
    price = Math.max(price + change, basePrice * 0.5);
    history.push({
      date: date.toISOString().split('T')[0],
      price: parseFloat(price.toFixed(2)),
      open:  parseFloat((price * 0.998).toFixed(2)),
      high:  parseFloat((price * 1.012).toFixed(2)),
      low:   parseFloat((price * 0.988).toFixed(2)),
      volume: Math.floor(Math.random() * 80_000_000 + 20_000_000),
    });
  }
  return history;
}

